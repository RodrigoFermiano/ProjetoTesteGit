USE [DB_PREFEITURA_CONECTA]
GO


SET ANSI_NULLS ON
GO

SET QUOTED_IDENTIFIER ON
GO

IF EXISTS ( SELECT * 
            FROM   SYSOBJECTS 
            WHERE  ID = OBJECT_ID(N'[DBO].[SP_SEL_PREFEITURAS]')  AND OBJECTPROPERTY(ID, N'ISPROCEDURE') = 1 )
BEGIN
    DROP PROCEDURE [DBO].[SP_SEL_PREFEITURAS]
END
GO

CREATE PROCEDURE [dbo].[SP_SEL_PREFEITURAS]

AS

BEGIN
	
	SET NOCOUNT ON
	

	SELECT 
	   [ID_PREFEITURA]
      ,[DESCRICAO]

  FROM
	  [dbo].[TB_PREFEITURA] WITH(NOLOCK)
		
 SET NOCOUNT OFF
END

GO

